public class VariableOverview {
    public static void main(String[] args) {
        // variables
        int a = 56;
        int b = 60;
        int sum = a + b;
        System.out.println("The sum of the variables is: " + sum);
        int diff = a - b;
        System.out.println("The diff of the variables is: " + diff);
    }
}
